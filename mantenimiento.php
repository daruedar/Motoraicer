<div class="std"><div class="educational" style="color: #000000;">
<p>
    Es muy bien sabido por los motociclistas que  es necesario realizar mantenimientos periódicos a las motos para minimizar averias que puedan ocasionar accidentes. Se debe  prestar atencional desgaste de las piezas, a la limpieza regular de la moto y al mantenimiento de cada uno de sus sistemas.
</p>
<h2 style="text-align: center;"><strong>KIT DE ARRASTRE</strong></h2>
<p>
    

Aunque en ocasiones lo ignoramos, el<strong> kit de arrastre</strong> es fundamental para que la moto obtenga la velocidad y la potencia para la cual fue diseñada.

El <strong>kit de arrastre</strong> esta formado por 4 piezas: piñón de salida o delantero, el trasero o sproket cadena y arandelas pinadoras. Cada una de éstas necesita un mantenimiento especial. El juego libre de la cadena debe ser de aproximadamente entre 20 y 30 mm en el centro y el cambio de uno de los componentes mencionados es causal para el cambio de todos.<br>

Por ejemplo, la cadena debe limpiarse periódicamente con un pincel o brocha, ACPM y un aceite de alta viscosidad (como el SAE 90) para que esta gire sin complicaciones. No se debe limpiar con combustibles o mezclas de ellos, ya que es suficiente hacerlo con una brocha con agua y buen jabón o un líquido desengrasante. La lubricación debe darse con lubricantes a base de grafito o molibdeno, los minerales del aceite nuevo o usado dañan causan desgaste prematuro en la cadena y está su vez en los demás componentes.<br>

Para el piñón de salida, se recomienda no olvidar el desmontaje del protector del piñón de salida de caja; ya que éste es uno de los puntos donde se aloja más suciedad y por donde la cadena puede adquirirla de forma uniforme a lo largo de su longitud.<br>
    
    
    Se debe remover por completo los residuos de grasa, aceite y/o polvo presentes en éste protector, secándolo completamente y volviendo a instalarlo en su lugar.<br>

La recomendación según los fabricantes es que se<strong> inspeccione, limpie, ajuste y lubrique cada 1000 kilómetros</strong> y que se cambie dependiendo del nivel de desgaste. En promedio se hace cada 30000 kilometros haciéndole limpieza, mantenimiento y ajuste cada 1000 Kilómetros. El tiempo exacto de cambio y mantenimiento se encuentra en el manual del propietario.
    </p>
<h2 style="text-align: center;"><strong>FILTRO DE ACEITE</strong></h2>
<p>
    El filtro de aceite garantiza  que el aceite que lubrica el motor, permanezca en optimas condiciones.
El color del aceite no debe ser oscuro o negro. Sí es así, se puede concluir que el aceite ha perdido sus cualidades de lubricación.Por tanto se recomienda su cambio inmediato tanto de aceite como de  <strong>filtro de aceite.</strong><br>
    El aceite no debe perder su olor característico, porque si huele a algo diferente al aceite es porque éste se puede estarse mezclando con combustible, agua u otros líquidos para limpiar el motor.
    Si el aceite presenta una textura viscosa, pastosa o con grumos, puede estar también mezclándose indebidamente con otros fluidos.<br> 

El aceite debería cambiarse cada 2000 kilómetros y el filtro de aceite cada 4000 (o según las especificaciones del manual del propietario de la moto). Si realizas trayectos a altas velocidades, realizas muchas paradas y arranques en cada viaje o utilizas un aceite de motor de baja viscosidad, el cambio de aceite debe realizarse más frecuentemente.
    
    </p>


<h2 style="text-align: center;"><strong>FILTRO DE AIRE Y GASOLINA</strong></h2>
<p>
 Para que ambos filtros funcionen perfectamente, es necesario realizarles mantenimiento periódicamente. Para evitar que suciedades e impurezas dañen estos filtros, la recomendación es cambiar el filtro de aire cada 10000 kilómetros aproximadamente, y si es de espuma cada 1000 kilómetros. El filtro de gasolina debe revisarse y realizarle mantenimiento cada 3000 kilómetros, y cambiarlo cada 10000 kilómetros aproximadamente. El tiempo exacto recambio y mantenimiento lo encuentra en el manual del propietario de su motocicleta.
    </p>
<h2 style="text-align: center;"><strong>CABLES Y GUAYAS</strong></h2>
<p>
    Los cables del velocímetro, acelerador, shock, frenos, etc. también necesitan un mantenimiento regular. Para ello, se necesita:

    Para lubricar las guayas, afloja el cable por completo en ambos extremos. Luego de que el cable se encuentre desensamblado, por uno de los extremos, comienza aplicar el lubricante con una jeringa y espera a que gotee por el otro extremo.
    Usa un aceite lubricante en spray de alta penetración con aplicaciones para aflojar tornillos, bisagras, cerraduras, etc. 
    </p>

